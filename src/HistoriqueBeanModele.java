
public class HistoriqueBeanModele {
	private int id;
	private int entree;
	private int sortie;
	private String date;
	
	public HistoriqueBeanModele() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getEntree() {
		return entree;
	}

	public void setEntree(int entree) {
		this.entree = entree;
	}

	public int getSortie() {
		return sortie;
	}

	public void setSortie(int sortie) {
		this.sortie = sortie;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
	

}
